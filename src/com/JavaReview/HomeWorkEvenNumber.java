package com.JavaReview;

public class HomeWorkEvenNumber {
	public static void main(String[] args) {
		int k = 42;

		if (k % 2 == 0) {
			System.out.println("Even");
		} else {
			System.out.println("Odd");
		}
		int n = 23;
		if (n % 2 == 0) {
			System.out.println("Even");
		} else {
			System.out.println("Odd");

		}
	}
}
